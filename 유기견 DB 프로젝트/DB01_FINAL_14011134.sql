CREATE TABLE VOLUNTEER(
VOLNO INT NOT NULL UNIQUE,
NAME CHAR(20),
PHONE CHAR(12) NOT NULL UNIQUE,
EMAIL CHAR(20),
SUPPORT INT NOT NULL DEFAULT 7,
PRIMARY KEY(VOLNO),
FOREIGN KEY(SUPPORT) REFERENCES EMPLOYEE(EMPNO)
ON DELETE SET DEFAULT ON UPDATE CASCADE
);

CREATE TABLE VETERINARIAN(
VETNO INT NOT NULL UNIQUE,
NAME CHAR(20) NOT NULL,
BELONG CHAR(25) NOT NULL,
PHONE CHAR(12) NOT NULL UNIQUE,
PRIMARY KEY(VETNO)
);

CREATE TABLE ABANDONDED_DOG(
DOGNO INT NOT NULL UNIQUE,
SPECIES CHAR(10) NOT NULL,
FUR_COLOR CHAR(10) NOT NULL,
SEX CHAR(6) NOT NULL,
WEIGHT INT NOT NULL,
MANAGED INT NOT NULL DEFAULT 7,
ENTERDATE CHAR(10) NOT NULL,
DISCOVER_CITY CHAR(10) NOT NULL,
DISCOVER_GU CHAR(10),
DISCOVER_DONG CHAR(10),
PRIMARY KEY(DOGNO),
FOREIGN KEY(MANAGED) REFERENCES EMPLOYEE(EMPNO)
ON DELETE SET DEFAULT ON UPDATE CASCADE
);

CREATE TABLE COMMUNITY(
ID INT NOT NULL UNIQUE,
NICKNAME CHAR(10) NOT NULL UNIQUE,
EMAIL CHAR(20) NOT NULL UNIQUE,
PHONE CHAR(12) NOT NULL UNIQUE,
ADDRESS_CITY CHAR(10) NOT NULL,
ADDRESS_GU CHAR(10),
ADDRESS_DONG CHAR(10),
PRIMARY KEY(ID)
);

CREATE TABLE FINDED_DOG(
DOGNO INT NOT NULL UNIQUE,
SPECIES CHAR(10) NOT NULL,
FUR_COLOR CHAR(10) NOT NULL,
SEX CHAR(6) NOT NULL,
POSTED INT NOT NULL,
FINDED_DATE CHAR(10),
DISCOVER_CITY CHAR(10),
DISCOVER_GU CHAR(10),
DISCOVER_DONG CHAR(10),
PRIMARY KEY(DOGNO),
FOREIGN KEY(POSTED) REFERENCES COMMUNITY(ID)
ON DELETE CASCADE ON UPDATE CASCADE,
);

CREATE TABLE LOST_DOG(
DOGNO INT NOT NULL UNIQUE,
SPECIES CHAR(10) NOT NULL,
FUR_COLOR CHAR(10) NOT NULL,
SEX CHAR(6) NOT NULL,
POSTED INT NOT NULL,
LOST_DATE CHAR(10) NOT NULL,
LOST_CITY CHAR(10) NOT NULL,
LOST_GU CHAR(10),
LOST_DONG CHAR(10),
PRIMARY KEY(DOGNO),
FOREIGN KEY(POSTED) REFERENCES COMMUNITY(ID)
ON DELETE CASCADE ON UPDATE CASCADE,
);

CREATE TABLE CARE(
DOG INT NOT NULL,
VETNO INT NOT NULL DEFAULT 1,
EMPNO INT NOT NULL DEFAULT 11,
CHECKUP CHAR(10),
VACCINE CHAR(10),
PRIMARY KEY(DOG, VETNO, EMPNO),
FOREIGN KEY(DOG) REFERENCES ABANDONDED_DOG(DOGNO)
ON DELETE NO ACTION ON UPDATE NO ACTION,
FOREIGN KEY(VETNO) REFERENCES VETERINARIAN(VETNO)
ON DELETE SET DEFAULT ON UPDATE CASCADE,
FOREIGN KEY(EMPNO) REFERENCES EMPLOYEE(EMPNO)
ON DELETE SET DEFAULT ON UPDATE CASCADE
);

